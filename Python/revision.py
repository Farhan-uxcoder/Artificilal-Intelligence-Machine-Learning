#finding a element in list
LS = [ 1,2,3,4,5]
index = 0
x = 3
for value in LS:
    if value==x:
        print(f"element found at {index+1} ")
        break
    index+=1

#tuple
tup = (1,2,3,4,5,6,7)
print(tup)
ind = 0
y = 4
for value in tup:
    if value == y:
        print(f"element found at {ind+1}th place")
        break
    ind+=1
    


